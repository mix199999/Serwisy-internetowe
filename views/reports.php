    var title1 = document.getElementById("title1").value;
    var id_user1 = document.getElementById("id_user1").value;
    var id_video1 = document.getElementById("id_video1").value;
    
    if (reason1 == "" || title1 == "") {
      window.alert("Values cannot be empty!");
    } else {
        $.ajax({  
        type: 'POST',  
        url: '/scripts/changePrivil.php', 
        data: {reason1: reason1, title1: title1, id_user1: id_user1, id_video1: id_video1},
        success: function(response) {
            console.log(response);
    }
});
    }
    
      });
  
  

